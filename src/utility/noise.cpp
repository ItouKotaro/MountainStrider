#include "noise.h"

//=============================================================
// [Noise] コンストラクタ
//=============================================================
Noise::Noise()
{
	m_value.resize(10);
	for (int i = 0; i < 10; i++)
		m_value[i].resize(10);
}

//=============================================================
// [Noise] サイズ設定
//=============================================================
void Noise::SetSize(const int& x, const int& y)
{
	if (x > 0 && y > 0)
	{
		m_value.resize(x);
		for (int i = 0; i < x; i++)
		{
			m_value[i].resize(y);
		}
	}
}

//=============================================================
// [Noise] データ取得
//=============================================================
int Noise::Get(const int& x, const int& y)
{
	if (0 <= x && x < GetSizeX() &&
		0 <= y && y < GetSizeY())
	{ // 範囲内のとき
		return m_value[x][y];
	}
	else
	{ // 範囲外のとき
		return 0;
	}
}

