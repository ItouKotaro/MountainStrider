//=============================================================
// [Gem] 更新
//=============================================================
void Gem::Update()
{
	// 回転
	transform->Rotate(0.0f, 0.02f, 0.0f);
}

//=============================================================
// [Gem] トリガー
//=============================================================
void Gem::OnTriggerEnter(GameObject* other)
{
	if (other->GetName() == "Vehicle")
	{
		ShopManager::AddPoint(1);
		gameObject->Destroy();
	}
}