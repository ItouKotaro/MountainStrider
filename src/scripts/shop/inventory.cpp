#include "inventory.h"

//=============================================================
// [InventoryUI] 初期化
//=============================================================
void InventoryUI::Init()
{
	// 背景
	m_bg = new SingleComponent<CPolygon>();
	m_bg->Init();
	m_bg->SetParent(gameObject);
	m_bg->transform->SetSize(600.0f, 100.0f);
}

//=============================================================
// [InventoryUI] 終了
//=============================================================
void InventoryUI::Uninit()
{
	m_bg->Uninit();
	delete m_bg;
}

//=============================================================
// [InventoryUI] 更新
//=============================================================
void InventoryUI::Update()
{
	m_bg->Update();
}

//=============================================================
// [InventoryUI] 描画
//=============================================================
void InventoryUI::DrawUI()
{
	m_bg->DrawUI();
}